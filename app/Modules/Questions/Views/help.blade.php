<h1>Types of exam questions</h1>

<p>Exams usually include different types of questions. Before you start studying for your exams make sure you know what type of questions to expect. Check your course materials or the course page. If you&#39;re still not sure ask your lecturer/tutor.</p>

<p>This section includes tips on how to study for and answer the most common types of questions.</p>
</div>

<h3>Short answer questions</h3>

<p>Short answer questions require a reasonably short answer &ndash; anything between a few words and a paragraph or two. The number of marks allocated often gives an indication of the length required.</p>

<p>When studying for short-answer questions, concentrate on:</p>

<ul>
	<li>terminology</li>
	<li>names</li>
	<li>facts</li>
	<li>concepts and theories, and examples underpinning them</li>
	<li>similarities and differences.</li>
</ul>

<p>When answering short questions:</p>

<ul>
	<li>Plan your answers before you start writing.</li>
	<li>Keep your answers short. It&rsquo;s not necessary to rewrite the question, and you shouldn&rsquo;t give more information than you have been asked for, as you won&rsquo;t get extra marks and may run out of time.</li>
	<li>Mark any questions you aren&#39;t sure of, and go back to them at the end of your exam if you have time.</li>
	<li>Try to answer all of the questions.&nbsp;</li>
</ul>

<p><span style="color:#00aeef; font-family:agenda_regular,trebuchet ms,arial narrow,helvetica,sans-serif; font-size:1.69em">Multiple-choice questions</span></p>

<p><span style="color:#000000">Multiple-choice questions consist of a question or the first half of a sentence (the stem), and a number of possible answers (usually between three and five). You have to choose the answer you think is correct from that list. Again, the number of marks allocated determines how long you should spend each question.</span></p>

<p>When studying for multiple-choice questions, concentrate on:</p>

<ul>
	<li>terminology</li>
	<li>names</li>
	<li>facts</li>
	<li>concepts and theories, and examples underpinning them</li>
	<li>similarities and differences.</li>
</ul>

<p>When answering multiple-choice questions</p>

<ul>
	<li>Quickly read all of the questions and their answers before you answering any.</li>
	<li>Mark the questions you aren&rsquo;t sure of so that you can come back to them if you have time.</li>
	<li>Answer the questions you&rsquo;re sure of first.</li>
	<li>Then try the others. Start by eliminating any answers that are obviously wrong.</li>
	<li>Watch out for negatives. For example, &lsquo;Which of these is not&hellip;?&rsquo;</li>
	<li>Stick to your time allocation. If your time&#39;s up and you still haven&#39;t decided on an answer guess or leave it out.&nbsp;</li>
	<li>Don&#39;t change your first answer unless you&#39;re really sure; your first instinctive choice is usually right.</li>
</ul>

<p>In most cases you should answer all of the questions even if you have to guess; if you choose something you may just be right. However, sometimes marks are deducted for wrong answers, so make sure you read the instructions very carefully before you start.</p>

<h3>Essay-type questions (long answers)</h3>

<p>Essay-type questions require an answer that is structured in the same way as an essay or report. They questions can be anything from a few paragraphs to a few pages. You don&#39;t have to include a reference list, but you should acknowledge your source(s). The mark allocation will often give an indication of the length required.</p>

<p>When studying for essay questions:</p>

<ul>
	<li>Try to identify possible questions you may be asked by reading past exam papers, corrected assignments and/or revision-type questions in your course material and textbook(s). However, check that the contents/format of the exam hasn&#39;t changed first.</li>
	<li>Work out model answers.</li>
	<li>Practise by writing answers under exam conditions. This means planning an answer, and writing it out within a timeframe.</li>
</ul>

<p>When answering essay questions in the exam:</p>

<ul>
	<li>Read the question carefully and analyse it so that you&#39;re sure you understand what it means.</li>
	<li>Brainstorm ideas and plan your answer. You could try using a mind map to help with this.</li>
	<li>Write down some key words. For example, your answer might have five main points, so jot them down, with a few key words under each point.</li>
	<li>Start your answer by briefly rephrasing the question using your own words.</li>
	<li>Use a new paragraph for each main idea or topic. Back up each topic with supporting detail such as examples, reasons and results.</li>
	<li>Leave a few lines of between each paragraph, as you may want to add additional information later.</li>
</ul>

<p>In essay-type questions it is important to stick to your time allocation. If you spend too much time on a question it may mean you run out of time for other questions. If you run out of time, jot down your main ideas and key words so that the examiner knows where you were going with the essay &ndash; you may get a few additional marks in this way.</p>

<p>You should also leave wide margins for the marker, and try to write neatly and proofread as you go.</p>

<h3>Problems/computational questions</h3>

<p>These types of question requires you to solve a problem using calculations.</p>

<p>When studying for problems/computational questions:</p>

<ul>
	<li>Learn the key vocabulary, theories and formulas, including how and when to apply the formulas.</li>
	<li>Look for practice questions in past exam papers, your course materials, set texts, etc.</li>
	<li>Practise answering this type of question in full, and writing each step down as if it were an exam.</li>
</ul>

<p>When answering problems/computational questions:</p>

<ul>
	<li>Read the questions and instructions very carefully before you start to ensure you know exactly what&#39;s required.</li>
	<li>Once you&rsquo;ve decided what you have to do, write down the formulas or methods you&rsquo;re going to use (if applicable).</li>
	<li>Show your workings. Even if your answer is wrong or incomplete you may still get some marks for showing you understand the process.</li>
	<li>Use a pencil for drawings and diagrams in case you need to change anything. If required, you can go over them with pen once you&#39;re satisfied.</li>
	<li>Label drawings and diagrams and include headings.</li>
</ul>

